//nolint:lll // the values are long and need to be in one line to make it readable
func TestOAuth_cacheInvalidation(t *testing.T) {
	tests := []struct {
		options    Options
		keys       []PublicKey
		err        error
		logMessage string
	}{
		{Options{
			ValidityFrequency: 10,
			JWKPath:           getTestServerURLWithInvalidJSON(),
		}, nil, middleware.ErrServiceDown, "Failed to unmarshal the json response body"},
		{
			options: Options{
				ValidityFrequency: 10,
				JWKPath:           getTestServerURL(),
			},
			keys: []PublicKey{{ID: "2011-04-29==", Alg: "RS256", Type: "RSA", Use: "", Operations: nil,
				Modulus:         "0vx7agoebGcQSuuPiLJXZptN9nndrQmbXEps2aiAFbWhM78LhWx4cbbfAAtVT86zwu1RK7aPFFxuhDR1L6tSoc_BJECPebWKRXjBZCiFV4n3oknjhMstn64tZ_2W-5JsGY4Hc5n9yBXArwl93lqt7_RN5w6Cf0h4QyQ5v-65YGjQR0_FDW2QvzqY368QQMicAtaSqzs8KJZgnYb9c7d0zgdAZHzu6qMQvRL5hajrn1n91CbOpbISD08qNLyrdkt-bFTWhAI4vMQFh6WeZu0fM4lFd2NcRwr3XPksINHaQ-G_xBniIqbw0Ls1jF44-csFCur-kEgU8awapJzKnqDKgw",
				PublicExponent:  "AQAB",
				PrivateExponent: "X4cTteJY_gn4FYPsXB8rdXix5vwsg1FLN5E3EaG6RJoVH-HLLKD9M7dx5oo7GURknchnrRweUkC7hT5fJLM0WbFAKNLWY2vv7B6NqXSzUvxT0_YSfqijwp3RTzlBaCxWp4doFk5N2o8Gy_nHNKroADIkJ46pRUohsXywbReAdYaMwFs9tv8d_cPVY3i07a3t8MN6TNwm0dSawm9v47UiCl3Sk5ZiG7xojPLu4sbg1U2jx4IBTNBznbJSzFHK66jT8bgkuqsk0GjskDJk19Z4qwjwbsnn4j2WBii3RL-Us2lGVkY8fkFzme1z0HbIkfz0Y6mqnOYtqc0X4jfcKoAC8Q",
				rsaPublicKey:    rsa.PublicKey{},
			}},
			err:        nil,
			logMessage: "",
		},
		{Options{ValidityFrequency: 10, JWKPath: "http://some-random-url"},

			nil, middleware.ErrServiceDown, "Failed to fetch the public key from the specified url",
		},
	}

	for i, tc := range tests {
		b := new(bytes.Buffer)
		logger := log.NewMockLogger(b)
		oAuth := New(logger, tc.options)
		err := oAuth.invalidateCache(logger)

		if err != tc.err {
			t.Errorf("Testcase %v  Failed. Error Expected %v\n Got %v\n", i, tc.err, err)
		}

		if !reflect.DeepEqual(oAuth.cache.publicKeys.Keys, tc.keys) {
			t.Errorf("Testcase %v Failed. Public keys Expected %v\nGot %v\n", i, tc.keys, oAuth.cache.publicKeys.Keys)
		}

		if !strings.Contains(b.String(), tc.logMessage) {
			t.Errorf("Testcase %v Failed. Log message Expected %v\nGot %v\n", i, tc.logMessage, b.String())
		}
	}
}
